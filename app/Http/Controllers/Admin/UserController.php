    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $image = User::saveImage($request->image);

        User::query()->create([
            'name' => $request->name,
            'email' => $request->email,
            'mobile' => $request->mobile,
            'password' => bcrypt($request->password),
            'image' => $image,
        ]);

        return to_route('users.index');
    }

